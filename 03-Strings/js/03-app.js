const nombre = "Emmanuel Ricardo"
const apellidoPaterno = 'Rincón'
const apellidoMaterno = "García"

// concatena concat
console.log(nombre.concat(apellidoPaterno).concat(apellidoMaterno));

//La manera de concatenar tradicionalmente es con el signo de +

console.log ("Mi nombre es " + nombre + " " + apellidoPaterno + " " + apellidoMaterno)


//La nueva manera de concatenar es con ${} y usando `` (Backticks)

console.log (`Mi nombre es ${nombre} ${apellidoPaterno} ${apellidoMaterno}`)

